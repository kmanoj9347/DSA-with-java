package Patterns;

public class Pattern_10 {
    public static void main(String[] args) {

        int n = 5;
        // number sqaure pattern.
        for (int i = 0; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }

}
